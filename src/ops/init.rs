const DEFAULT_PROJECT_KIT_TEMPLATE: &str = include_str!(concat!(
    env!("CARGO_MANIFEST_DIR"),
    "/docs/PROJECT_KIT_TEMPLATE.md"
));
const DEFAULT_AI_GUIDE_TEMPLATE: &str = include_str!(concat!(
    env!("CARGO_MANIFEST_DIR"),
    "/docs/AI_PROJECT_TEMPLATE.md"
));

#[derive(Debug, Serialize)]
struct InitSummary {
    created_at: String,
    force: bool,
    refresh_forbid: bool,
    language: String,
    wrote: Vec<String>,
    skipped: Vec<String>,
    zip_bundle: Option<String>,
}

#[derive(Debug, Clone)]
struct GeneratedMetadata {
    repo_name: String,
    branch: String,
    head: Option<String>,
    forbid_patterns: Vec<String>,
    forbid_suggestions: Vec<String>,
    preferred_target_branch: String,
    read_first_files: Vec<String>,
    detected_stack: Vec<String>,
    starter_commands: Vec<(String, String)>,
}

#[derive(Debug, Serialize)]
struct RulesZipMetadata {
    generated_at: String,
    run_id: String,
    language: String,
    branch: String,
    head: Option<String>,
    forbid_patterns: Vec<String>,
}

pub fn cmd(git_root: &Path, args: InitArgs) -> Result<(), ExitError> {
    let created_at = lock::now_rfc3339();
    if args.out.is_some() && !args.zip {
        return Err(ExitError::new(EXIT_GENERAL, "--out requires --zip"));
    }

    let lock_path = lock::default_lock_path(git_root);
    let cli_args = build_cli_args(
        args.force,
        args.refresh_forbid,
        args.template_dir.as_deref(),
        args.lang,
        args.zip,
        args.out.as_deref(),
    );
    let info = lock::make_lock_info(git_root, "init", &cli_args);
    let _guard = lock::LockGuard::acquire(&lock_path, info)?;

    // Always ensure .diffship exists (repo already ships it, but init should be safe everywhere).
    let diffship_dir = git_root.join(".diffship");
    fs::create_dir_all(&diffship_dir)
        .map_err(|e| ExitError::new(EXIT_GENERAL, format!("failed to create .diffship/: {e}")))?;

    // Create a run directory (M0 spine).
    let run_meta = run::create_run(git_root, "init", &cli_args, created_at.clone())?;
    let run_dir = run::run_dir(git_root, &run_meta.run_id);
    let cwd = std::env::current_dir()
        .map_err(|e| ExitError::new(EXIT_GENERAL, format!("failed to detect current dir: {e}")))?;
    let template_dir = args
        .template_dir
        .as_deref()
        .map(|raw| resolve_user_path(&cwd, raw))
        .transpose()?;
    let zip_out = args
        .out
        .as_deref()
        .map(|raw| resolve_user_path(&cwd, raw))
        .transpose()?;

    let mut wrote = vec![];
    let mut skipped = vec![];
    let metadata = collect_generated_metadata(git_root)?;
    let metadata_block = metadata.markdown_block();
    let snapshot_block = metadata.snapshot_block();
    let next_steps_block = metadata.next_steps_block();

    // 1) Project kit guide (derived from template)
    let kit_out = diffship_dir.join("PROJECT_KIT.md");
    let kit_src = load_template_text(
        template_dir.as_deref(),
        git_root.join("docs").join("PROJECT_KIT_TEMPLATE.md"),
        "PROJECT_KIT_TEMPLATE.md",
        DEFAULT_PROJECT_KIT_TEMPLATE,
    )?;

    let kit_body = format!(
        "# DiffshipOS Project Kit\n\n> Generated by `diffship init`\n\n{}\n{}\n{}\n{}",
        snapshot_block, next_steps_block, kit_src, metadata_block
    );

    write_if_needed(&kit_out, &kit_body, args.force, &mut wrote, &mut skipped)?;

    // 2) Paste-ready project rules snippet.
    let rules_out = diffship_dir.join("PROJECT_RULES.md");
    let rules_body = project_rules_body(&metadata, args.lang);
    write_if_needed(
        &rules_out,
        &rules_body,
        args.force,
        &mut wrote,
        &mut skipped,
    )?;

    // 3) AI-targeted guide (derived from template)
    let ai_out = diffship_dir.join("AI_GUIDE.md");
    let ai_src = load_template_text(
        template_dir.as_deref(),
        git_root.join("docs").join("AI_PROJECT_TEMPLATE.md"),
        "AI_PROJECT_TEMPLATE.md",
        DEFAULT_AI_GUIDE_TEMPLATE,
    )?;
    let ai_body = format!(
        "# DiffshipOS AI Guide\n\n> Generated by `diffship init`\n\n{}\n{}\n{}",
        snapshot_block, ai_src, metadata_block
    );
    write_if_needed(&ai_out, &ai_body, args.force, &mut wrote, &mut skipped)?;

    // 4) Dedicated forbid patterns stub
    let forbid_out = diffship_dir.join("forbid.toml");
    let forbid_body = default_forbid_stub(&metadata);
    write_if_needed(
        &forbid_out,
        &forbid_body,
        args.force || args.refresh_forbid,
        &mut wrote,
        &mut skipped,
    )?;

    // 5) Diffship-local gitignore for generated state
    let gitignore_out = diffship_dir.join(".gitignore");
    let gitignore_body = default_gitignore();
    write_if_needed(
        &gitignore_out,
        &gitignore_body,
        args.force,
        &mut wrote,
        &mut skipped,
    )?;

    // 6) Config stub
    let cfg_out = diffship_dir.join("config.toml");
    let cfg_body = default_config_stub(&metadata);
    write_if_needed(&cfg_out, &cfg_body, args.force, &mut wrote, &mut skipped)?;

    let zip_bundle = if args.zip {
        Some(write_rules_zip(
            &diffship_dir,
            &run_meta.run_id,
            &created_at,
            args.lang,
            &metadata,
            zip_out.as_deref(),
        )?)
    } else {
        None
    };

    // Write init summary under the run dir
    let summary = InitSummary {
        created_at,
        force: args.force,
        refresh_forbid: args.refresh_forbid,
        language: args.lang.as_str().to_string(),
        wrote: wrote.clone(),
        skipped: skipped.clone(),
        zip_bundle: zip_bundle.as_ref().map(|path| path.display().to_string()),
    };

    let bytes = serde_json::to_vec_pretty(&summary)
        .map_err(|e| ExitError::new(EXIT_GENERAL, format!("failed to encode init summary: {e}")))?;
    fs::write(run_dir.join("init.json"), bytes)
        .map_err(|e| ExitError::new(EXIT_GENERAL, format!("failed to write init.json: {e}")))?;

    // Human output
    println!("diffship init: ok");
    println!("  git_root : {}", git_root.display());
    println!("  run_id   : {}", run_meta.run_id);
    for p in &wrote {
        println!("  wrote    : {}", p);
    }
    for p in &skipped {
        println!("  skipped  : {}", p);
    }
    if let Some(path) = &zip_bundle {
        println!("  zip      : {}", path.display());
    }

    Ok(())
}

fn load_template_text(
    template_dir: Option<&Path>,
    repo_default: PathBuf,
    file_name: &str,
    built_in: &str,
) -> Result<String, ExitError> {
    if let Some(dir) = template_dir {
        let path = dir.join(file_name);
        if path.is_file() {
            return fs::read_to_string(&path).map_err(|e| {
                ExitError::new(
                    EXIT_GENERAL,
                    format!("failed to read template {}: {e}", path.display()),
                )
            });
        }
    }

    match fs::read_to_string(&repo_default) {
        Ok(s) => Ok(s),
        Err(e) if e.kind() == std::io::ErrorKind::NotFound => Ok(built_in.to_string()),
        Err(e) => Err(ExitError::new(
            EXIT_GENERAL,
            format!("failed to read template {}: {e}", repo_default.display()),
        )),
    }
}

fn build_cli_args(
    force: bool,
    refresh_forbid: bool,
    template_dir: Option<&str>,
    lang: InitLanguage,
    zip: bool,
    out: Option<&str>,
) -> Vec<String> {
    let mut v = vec![];
    if force {
        v.push("--force".to_string());
    }
    if refresh_forbid {
        v.push("--refresh-forbid".to_string());
    }
    if let Some(path) = template_dir {
        v.push(format!("--template-dir={path}"));
    }
    if lang != InitLanguage::En {
        v.push(format!("--lang={}", lang.as_str()));
    }
    if zip {
        v.push("--zip".to_string());
    }
    if let Some(path) = out {
        v.push(format!("--out={path}"));
    }
    v
}

fn collect_generated_metadata(git_root: &Path) -> Result<GeneratedMetadata, ExitError> {
    let repo_name = git_root
        .file_name()
        .map(|value| value.to_string_lossy().to_string())
        .filter(|value| !value.is_empty())
        .unwrap_or_else(|| git_root.display().to_string());
    let head = git::run_git(git_root, ["rev-parse", "HEAD"])
        .ok()
        .map(|s| s.trim().to_string())
        .filter(|s| !s.is_empty());
    let branch = git::run_git(git_root, ["symbolic-ref", "--quiet", "--short", "HEAD"])
        .ok()
        .map(|s| s.trim().to_string())
        .filter(|s| !s.is_empty())
        .unwrap_or_else(|| "(detached)".to_string());
    let preferred_target_branch = detect_preferred_target_branch(git_root, &branch);
    let forbid_patterns =
        config::resolve_ops_config(git_root, None, Default::default())?.forbid_patterns();
    let forbid_suggestions = detect_forbid_suggestions(git_root);
    Ok(GeneratedMetadata {
        repo_name,
        branch,
        head,
        forbid_patterns,
        forbid_suggestions,
        preferred_target_branch,
        read_first_files: detect_read_first_files(git_root),
        detected_stack: detect_stack(git_root),
        starter_commands: detect_starter_commands(git_root),
    })
}

fn write_if_needed(
    path: &std::path::Path,
    body: &str,
    force: bool,
    wrote: &mut Vec<String>,
    skipped: &mut Vec<String>,
) -> Result<(), ExitError> {
    if path.exists() && !force {
        skipped.push(path.display().to_string());
        return Ok(());
    }

    fs::write(path, body).map_err(|e| {
        ExitError::new(
            EXIT_GENERAL,
            format!("failed to write {}: {e}", path.display()),
        )
    })?;
    wrote.push(path.display().to_string());
    Ok(())
}

